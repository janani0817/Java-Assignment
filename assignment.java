//2.explain implicit type casting
// java automatically converts a smaller data type into larger data
// byte-short-int-long-float-double
// example;
class ImplicitCasting {
    public static void main(String[] args) {
        int a = 25;      
        double b = a; 

        System.out.println("Integer value: " + a);
        System.out.println("Double value: " + b);
    }
}
//3 explain explicit type casting
//converts a larger data type into a smaller data type by specifying the target type.
//double → float → long → int → short → byte
// example;
class ExplicitCasting {
    public static void main(String[] args) {
        double a = 45.89;      
        int b = (int) a;      

        System.out.println("Double value: " + a);
        System.out.println("Integer value: " + b);
    }
}

